def maze_to_commands(maze):
    """
    Converts a 32x32 maze (list of lists) to a series of "draw" commands.

    Args:
        maze: A 32x32 list of lists representing the maze. 1 for wall, 0 for floor.

    Returns:
        A list of strings, where each string is a "draw" command.
    """

    commands = []
    visited = [[False for _ in range(32)] for _ in range(32)]

    for r in range(32):
        for c in range(32):
            if maze[r][c] == 1 and not visited[r][c]:
                command = f"Start at ({r},{c}): "
                current_r, current_c = r, c
                visited[r][c] = True
                sequence = []

                while True:
                    best_direction = None
                    best_length = 0

                    for dr, dc, direction_name in [(0, 1, "right"), (0, -1, "left"), (1, 0, "down"), (-1, 0, "up"), (1,1,"right-down"),(1,-1,"left-down"),(-1,1,"right-up"),(-1,-1,"left-up")]:
                        length = 0
                        for i in range(1, 128): #check for 127 blocks max
                            nr, nc = current_r + dr * i, current_c + dc * i
                            if 0 <= nr < 32 and 0 <= nc < 32 and maze[nr][nc] == 1 and not visited[nr][nc]:
                                length += 1
                            else:
                                break

                        if length > best_length:
                            best_length = length
                            best_direction = (dr, dc, direction_name)

                    if best_direction:
                        dr, dc, direction_name = best_direction
                        for i in range(1, best_length + 1):
                            nr, nc = current_r + dr * i, current_c + dc * i
                            visited[nr][nc] = True
                        sequence.append((best_length, direction_name))
                        current_r += dr * best_length
                        current_c += dc * best_length
                    else:
                        break

                # Format the command string
                for length, direction_name in sequence:
                    command += f"draw {length} blocks {direction_name}, "
                commands.append(command[:-2]) # Remove the trailing comma and space

    return commands


